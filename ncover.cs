            }

            if (jsonOutput is not null)
            {
                await using var file = jsonOutput.Open(FileMode.Create, FileAccess.Write);
                await JsonSerializer.SerializeAsync(file, report, 
                    pretty
                        ? ReportDataPrettySerializerContext.Default.ReportData
                        : ReportDataSerializerContext.Default.ReportData,
                    cancellationToken
                );
            }

            return 0;
        }
        finally
        {
            nuGetPackages?.Dispose();
        }
    }
    catch (Exception ex)
    {
        var (verbosity, outputFormat) = errorOptions ?? (Verbosity.Default, new OutputFormat(useAnsi: true, useUnicode: true));

        switch (verbosity)
        {
            case Verbosity.None:
            case Verbosity.Json:
                // verbosity levels None and Json also suppress error output
                break;

            case Verbosity.Default:
                await Error.WriteLineAsync($"{outputFormat.Red}[{outputFormat.Reset}{outputFormat.Bold}{outputFormat.Red}{ex.GetType().Name}{outputFormat.Reset}{outputFormat.Red}]{(!string.IsNullOrWhiteSpace(ex.Message) ? $": {ex.Message}" : string.Empty)}{outputFormat.Reset}".AsMemory(), cancellationToken);
