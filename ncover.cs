    public static async ValueTask<PeFile> OpenAsync(FileInfo file, CancellationToken cancellationToken = default)
    {
        const int dosHeaderMagicOffset = 0x00,
                  dosHeaderELfanewOffset = 0x3C,
                  fileHeaderNumberOfSectionsOffset = 2,
                  fileHeaderSizeOfOptionalHeaderOffset = 16,
                  fileHeaderSize = 20,
                  optionalHeaderMagicOffset = 0x00,
                  optionalHeader32DataDirectoryOffset = 96,
                  optionalHeader64DataDirectoryOffset = 112,
                  optionalHeaderSizeOfHeadersOffset = 60,
                  dataDirectoryEntrySize = 8,
                  dataDirectoryEntryExportIndex = 0,
                  dataDirectoryEntryResourceIndex = 2,
                  dataDirectoryEntryComIndex = 14;

        const ushort optionalHeaderMagicPE32 = 0x10B,
                     optionalHeaderMagicPE32Plus = 0x20B;
        
        const int bufferSize = 256;

        ArgumentNullException.ThrowIfNull(file);

        var stream = file.OpenRead();
        try
        {        
            var buffer = GC.AllocateUninitializedArray<byte>(bufferSize).AsMemory();

            // DOS Header
            stream.Seek(dosHeaderMagicOffset, SeekOrigin.Begin);
            await stream.ReadExactlyAsync(buffer[..2], cancellationToken);

            if (buffer.Span is not [(byte)'M', (byte)'Z', ..]) { throw new InvalidDataException("Missing MZ header."); }

            stream.Seek(dosHeaderELfanewOffset, SeekOrigin.Begin);
            var eLfanew = await stream.ReadLittleEndianAsync<int>(cancellationToken);

            // NT Headers
            stream.Seek(eLfanew, SeekOrigin.Begin);
            await stream.ReadExactlyAsync(buffer[..4], cancellationToken);

            if (buffer.Span is not [(byte)'P', (byte)'E', (byte)'\0', (byte)'\0', ..]) { throw new InvalidDataException("Missing PE\\0\\0 signature."); }
            
            // File Header
            var fileHeaderOffset = eLfanew + 4;

            stream.Seek(fileHeaderOffset + fileHeaderNumberOfSectionsOffset, SeekOrigin.Begin);
            var numberOfSections = await stream.ReadLittleEndianAsync<ushort>(cancellationToken);

            stream.Seek(fileHeaderOffset + fileHeaderSizeOfOptionalHeaderOffset, SeekOrigin.Begin);
            var sizeOfOptionalHeader = await stream.ReadLittleEndianAsync<ushort>(cancellationToken);

            // Optional Header
            var optionalHeaderOffset = fileHeaderOffset + fileHeaderSize;

            stream.Seek(optionalHeaderOffset + optionalHeaderMagicOffset, SeekOrigin.Begin);
            var optionalHeaderMagic = await stream.ReadLittleEndianAsync<ushort>(cancellationToken);

            var optionalHeaderDataDirectoyOffset = optionalHeaderMagic switch
            {
                optionalHeaderMagicPE32 => optionalHeader32DataDirectoryOffset,
                optionalHeaderMagicPE32Plus => optionalHeader64DataDirectoryOffset,
                _ => throw new InvalidDataException($"Unknown optional header magic: 0x{optionalHeaderMagic:X}")
            };

            stream.Seek(optionalHeaderOffset + optionalHeaderSizeOfHeadersOffset, SeekOrigin.Begin);
            var sizeOfHeaders = await stream.ReadLittleEndianAsync<uint>(cancellationToken);

            var dataDirectoryEntryOffset = optionalHeaderOffset + optionalHeaderDataDirectoyOffset;

            // Export Directory
            stream.Seek(dataDirectoryEntryOffset + (dataDirectoryEntryExportIndex * dataDirectoryEntrySize), SeekOrigin.Begin);
            var exportRva = await stream.ReadLittleEndianAsync<uint>(cancellationToken);
            var exportSize = await stream.ReadLittleEndianAsync<uint>(cancellationToken);

            // Resource Directory
            stream.Seek(dataDirectoryEntryOffset + (dataDirectoryEntryResourceIndex * dataDirectoryEntrySize), SeekOrigin.Begin);
            var resourceRva = await stream.ReadLittleEndianAsync<uint>(cancellationToken);
            var resourceSize = await stream.ReadLittleEndianAsync<uint>(cancellationToken);

            // COM Directory
            stream.Seek(dataDirectoryEntryOffset + (dataDirectoryEntryComIndex * dataDirectoryEntrySize), SeekOrigin.Begin);
            var comRva = await stream.ReadLittleEndianAsync<uint>(cancellationToken);
            var comSize = await stream.ReadLittleEndianAsync<uint>(cancellationToken);

            // Decide which PeFile kind to create
            if (comRva is not 0 && comSize is not 0)
            {
                // This seems to be a .NET assembly

                // Make sure to close the file stream before creating the 'Managed' PeFile, as it will open the file again
                // We don't transfer ownership of the stream here, and we need to close it now, so we dispose it ourselves
                await stream.DisposeAsync();
                stream = null;

                return await CreateAsync<Managed, FileInfo>(file, cancellationToken);                
            }
            else
            {
                // This seems to be a native PE

                var args = new NativeArgs(
                    Stream: stream,
                    Buffer: buffer,
                    NumberOfSections: numberOfSections,
                    SizeOfHeaders: sizeOfHeaders,
                    ExportDirectoryEntryRva: exportRva,
                    ExportDirectoryEntrySize: exportSize,
                    ResourceDirectoryEntryRva: resourceRva,
                    ResourceDirectoryEntrySize: resourceSize,
                    SectionTableOffset: (long)optionalHeaderOffset + (long)sizeOfOptionalHeader
                );

                // 'Native' PeFile will take ownership of the stream 
                stream = null;

                try
                {
                    return await CreateAsync<Native, NativeArgs>(args, cancellationToken);
                }
                catch
                {
                    // If the creation of the 'Native' PeFile failed, we need to take back ownership of the stream in order to dispose it ourselves
                    stream = args.Stream;
                    throw;
                }
            }
        }
        finally
        {
            if (stream is not null)
            {                
                // Dispose the stream if we didn't transfer its ownership
                // This path is unused as of now, but it's here for correctness and future-proofing

                await stream.DisposeAsync();
            }
        }
    }

	public static ValueTask<PeFile> OpenAsync(string fileName, CancellationToken cancellationToken = default)
        => OpenAsync(file: new(fileName), cancellationToken);
