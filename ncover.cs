    public static async Task<NuGetPackagesResolver?> LookupAndDownloadAsync(string packageTerm, bool preferX64 = true, string? nuGetSource = "https://api.nuget.org/v3/index.json",
        Func<string, VersionRange?, CancellationToken, Task>? onBeginPackageLookupAsync = null, Func<PackageIdentity, CancellationToken, Task>? onEndPackageLookupAsync = null,
        Func<PackageIdentity, CancellationToken, Task>? onBeginDependencyPackagesLookupAsync = null, Func<PackageIdentity, string, PackageIdentity, CancellationToken, Task>? onEndDependencyPackagesLookupAsync = null,
        Func<PackageIdentity, string, CancellationToken, Task>? onBeginDownloadRidPackageAsync = null, Func<PackageIdentity, string, CancellationToken, Task>? onEndDownloadRidPackageAsync = null,
        Func<PackageIdentity, string, CancellationToken, Task>? onBeginExtractNativeLibraryAsync = null, Func<PackageIdentity, string, string, CancellationToken, Task>? onEndExtractNativeLibraryAsync = null,
        Func<PackageIdentity, CancellationToken, Task>? onBeginDownloadCorePackageAsync = null, Func<PackageIdentity, CancellationToken, Task>? onEndDownloadCorePackageAsync = null,
        Func<PackageIdentity, CancellationToken, Task>? onBeginExtractManagedLibraryAsync = null, Func<PackageIdentity, string, CancellationToken, Task>? onEndExtractManagedLibraryAsync = null,
        CancellationToken cancellationToken = default)
    {
        ArgumentException.ThrowIfNullOrWhiteSpace(packageTerm, nameof(packageTerm));
        ArgumentException.ThrowIfNullOrWhiteSpace(nuGetSource, nameof(nuGetSource));

        var packageTermSpan = packageTerm.AsSpan();
        var atIndex = packageTermSpan.IndexOf('@');

        VersionRange? packageVersionRange;
        if (atIndex is < 0)
        {
            packageVersionRange = null;
        }
        else
        {
            var packageVersionPart = packageTermSpan[(atIndex + 1)..].Trim().ToString();

            if (!VersionRange.TryParse(packageVersionPart, out packageVersionRange)) { throw new ArgumentException($"The package version part \"{packageVersionPart}\" of the package term is invalid. Please specify a valid version range (e.g., \"1.0.*-*\", \"[1.0.0, 2.0.0)\", just \"1\") or leave it out entirely.", nameof(packageTerm)); }

            packageTermSpan = packageTermSpan[..atIndex];
        }        

        packageTermSpan = packageTermSpan.Trim();

        if (packageTermSpan.IsEmpty) { throw new ArgumentException($"The package ID part of the package term cannot be empty. Please specify a valid package term (e.g., \"Sdl3Sharp@1.0.*-*\" or just \"Sdl3Sharp\").", nameof(packageTerm));}

        var packageId = packageTermSpan.ToString();

        /* *** */

        if (onBeginPackageLookupAsync is not null) { await onBeginPackageLookupAsync(packageId, packageVersionRange, cancellationToken); }

        var repository = Repository.Factory.GetCoreV3(nuGetSource);
        var findPackageByIdResource = await repository.GetResourceAsync<FindPackageByIdResource>(cancellationToken);
        var cacheContext = new SourceCacheContext() { NoCache = true, DirectDownload = true };

        var allVersions = await findPackageByIdResource.GetAllVersionsAsync(packageId, cacheContext, NullLogger.Instance, cancellationToken);
        var bestVersion = packageVersionRange is null
            ? allVersions.Where(static v => !v.IsPrerelease).OrderDescending().FirstOrDefault()
            : packageVersionRange.FindBestMatch(allVersions);

        if (bestVersion is null) { throw new InvalidOperationException($"Could not find meta package \"{packageId}\"{(packageVersionRange is not null ? $" with version range {packageVersionRange}" : string.Empty)}."); }

        var metaPackage = new PackageIdentity(packageId, bestVersion);

        if (onEndPackageLookupAsync is not null) { await onEndPackageLookupAsync(metaPackage, cancellationToken); }

        /* *** */

        if (onBeginDependencyPackagesLookupAsync is not null) { await onBeginDependencyPackagesLookupAsync(metaPackage, cancellationToken); }

        var metaDependencyInfo = await findPackageByIdResource.GetDependencyInfoAsync(metaPackage.Id, metaPackage.Version, cacheContext, NullLogger.Instance, cancellationToken);

        var ridDependency = metaDependencyInfo.DependencyGroups
            .SelectMany(static g => g.Packages)
            .Where(d => d.Id.Equals($"{metaPackage.Id}.win-x64", StringComparison.OrdinalIgnoreCase) || d.Id.Equals($"{metaPackage.Id}.win-x86", StringComparison.OrdinalIgnoreCase))
            .Where(d => d.VersionRange.Satisfies(metaPackage.Version))
            .OrderBy(d => d.Id.EndsWith("x64", StringComparison.OrdinalIgnoreCase) ? (preferX64 ? 0 : 1) : (preferX64 ? 1 : 0))
            .FirstOrDefault();

        if (ridDependency is null) { throw new InvalidOperationException($"Could not find a RID-specific package for meta package \"{metaPackage}\". Expected a dependency with an ID of either \"{metaPackage.Id}.win-x64\" or \"{metaPackage.Id}.win-x86\"."); }

        var ridPackage = new PackageIdentity(ridDependency.Id, metaPackage.Version);
        var rid = ridDependency.Id.EndsWith("win-x64", StringComparison.OrdinalIgnoreCase) ? "win-x64" : "win-x86";
        
        var ridDependencyInfo = await findPackageByIdResource.GetDependencyInfoAsync(ridDependency.Id, metaPackage.Version, cacheContext, NullLogger.Instance, cancellationToken);

        var coreDependency = ridDependencyInfo.DependencyGroups
            .SelectMany(static g => g.Packages)
            .Where(d => d.Id.Equals($"{metaPackage.Id}.Core", StringComparison.OrdinalIgnoreCase))
            .Where(d => d.VersionRange.Satisfies(metaPackage.Version))
            .FirstOrDefault();

        if (coreDependency is null) { throw new InvalidOperationException($"Could not find a core package dependency for RID-specific package \"{ridPackage}\". Expected a dependency with an ID of \"{metaPackage.Id}.Core\"."); }

        var corePackage = new PackageIdentity(coreDependency.Id, metaPackage.Version);

        if (onEndDependencyPackagesLookupAsync is not null) { await onEndDependencyPackagesLookupAsync(ridPackage, rid, corePackage, cancellationToken); }

        /* *** */        

        var needsManualCleanup = true;
        string tempDir;
        do
        {
            tempDir = Path.Combine(Path.GetTempPath(), Path.GetRandomFileName());
        }
        while (Directory.Exists(tempDir));

        Directory.CreateDirectory(tempDir);
        try
        {   
            if (onBeginDownloadRidPackageAsync is not null) { await onBeginDownloadRidPackageAsync(ridPackage, rid, cancellationToken); }

            string nativeLibraryFile;
            using (var ridPackageDownloader = await findPackageByIdResource.GetPackageDownloaderAsync(ridPackage, cacheContext, NullLogger.Instance, cancellationToken))
            {
                await ridPackageDownloader.CopyNupkgFileToAsync(Path.Combine(tempDir, $"{ridPackage}.nupkg"), cancellationToken);

                if (onEndDownloadRidPackageAsync is not null) { await onEndDownloadRidPackageAsync(ridPackage, rid, cancellationToken); }

                /* *** */

                if (onBeginExtractNativeLibraryAsync is not null) { await onBeginExtractNativeLibraryAsync(ridPackage, rid, cancellationToken); }

                var internalNativeLibraryFilePath = (await ridPackageDownloader.CoreReader.GetFilesAsync(cancellationToken))
                    .Where(f => f.StartsWith($"runtimes/{rid}/native/", StringComparison.OrdinalIgnoreCase) && f.EndsWith(".dll", StringComparison.OrdinalIgnoreCase))
                    .FirstOrDefault();

                if (internalNativeLibraryFilePath is null) { throw new InvalidOperationException($"RID-specific package \"{ridPackage}\" for RID \"{rid}\" does not contain a native library. Expected a file in the package with a path of the form \"runtimes/{rid}/native/*.dll\".");}

                nativeLibraryFile = Path.Combine(tempDir, Path.GetFileName(internalNativeLibraryFilePath));

                await using var destinationStream = File.Create(nativeLibraryFile);
                await using var sourceStream = await ridPackageDownloader.CoreReader.GetStreamAsync(internalNativeLibraryFilePath, cancellationToken);

                await sourceStream.CopyToAsync(destinationStream, cancellationToken);
            }            

            if (onEndExtractNativeLibraryAsync is not null) { await onEndExtractNativeLibraryAsync(ridPackage, rid, Path.GetFileName(nativeLibraryFile), cancellationToken); }
            
            /* *** */

            if (onBeginDownloadCorePackageAsync is not null) { await onBeginDownloadCorePackageAsync(corePackage, cancellationToken); }

            string managedLibraryFile;
            using (var corePackageDownloader = await findPackageByIdResource.GetPackageDownloaderAsync(corePackage, cacheContext, NullLogger.Instance, cancellationToken))
            {
                await corePackageDownloader.CopyNupkgFileToAsync(Path.Combine(tempDir, $"{corePackage}.nupkg"), cancellationToken);

                if (onEndDownloadCorePackageAsync is not null) { await onEndDownloadCorePackageAsync(corePackage, cancellationToken); }

                /* *** */

                if (onBeginExtractManagedLibraryAsync is not null) { await onBeginExtractManagedLibraryAsync(corePackage, cancellationToken); }

                var internalManagedLibraryFilePath = (await corePackageDownloader.CoreReader.GetFilesAsync(cancellationToken))
                    .Where(f => f.StartsWith("lib/", StringComparison.OrdinalIgnoreCase) && f["lib/".Length..].Contains('/') && f.EndsWith(".dll", StringComparison.OrdinalIgnoreCase))
                    .FirstOrDefault();

                if (internalManagedLibraryFilePath is null) { throw new InvalidOperationException($"Core package \"{corePackage}\" does not contain a managed library. Expected a file in the package with a path of the form \"lib/*/*.dll\".");}

                managedLibraryFile = Path.Combine(tempDir, Path.GetFileName(internalManagedLibraryFilePath));
                
                await using var destinationStream = File.Create(managedLibraryFile);
                await using var sourceStream = await corePackageDownloader.CoreReader.GetStreamAsync(internalManagedLibraryFilePath, cancellationToken);

                await sourceStream.CopyToAsync(destinationStream, cancellationToken);
            }            

            if (onEndExtractManagedLibraryAsync is not null) { await onEndExtractManagedLibraryAsync(corePackage, Path.GetFileName(managedLibraryFile), cancellationToken); }

            /* *** */

            needsManualCleanup = false;

            return new NuGetPackagesResolver(ridPackage, rid, corePackage, packageTerm, nuGetSource, tempDir, nativeLibraryFile, managedLibraryFile);
        }
        finally
        {
            if (needsManualCleanup)
            {
                try { Directory.Delete(tempDir, recursive: true); } catch { }
            }
        }
    }
}

file abstract class PeFile : IAsyncDisposable, IDisposable
{
	private interface IPeFile<TSelf, TArg>
		where TSelf : PeFile, IPeFile<TSelf, TArg>
	{
		static abstract ValueTask<PeFile> CreateAsync(TArg arg, CancellationToken cancellationToken = default);
	}

	public sealed class Managed : PeFile, IPeFile<Managed, FileInfo>
	{
        private MetadataLoadContext? mMetadataLoadContext;
        private readonly Assembly mAssembly;

		private Managed(MetadataLoadContext metadataLoadContext, Assembly assembly, FileInfo file) : base(file)
        {
            mMetadataLoadContext = metadataLoadContext;
            mAssembly = assembly;
        }

        public Assembly Assembly => mAssembly;

        protected override void Dispose(bool disposing)
        {
            if (disposing && mMetadataLoadContext is not null)
            {
                mMetadataLoadContext.Dispose();
                mMetadataLoadContext = null;
            }
        }

        [UnconditionalSuppressMessage(
            "Trimming",
            "IL2026:RequiresUnreferencedCode",
            Justification = "Tool uses reflection over arbitrary assemblies; not intended to be trimmed.")]
        [UnconditionalSuppressMessage(
            "Trimming",
            "IL2075:DynamicallyAccessedMembers",
            Justification = "Tool uses reflection over arbitrary assemblies; not intended to be trimmed.")]
		public IEnumerable<(MethodInfo Method, string SymbolName, Type? ConditionType)> EnumerateImportedSymbols()
		{
            const BindingFlags bindingFlags = BindingFlags.Public | BindingFlags.NonPublic
                                            | BindingFlags.Instance | BindingFlags.Static
                                            | BindingFlags.DeclaredOnly;

            if (mMetadataLoadContext is null) { throw new ObjectDisposedException(nameof(Managed)); }
            
            foreach (var type in mAssembly.GetTypes())
            {
                foreach (var method in type.GetMethods(bindingFlags))
                {
                    foreach (var attributeData in method.CustomAttributes)
                    {
                        var attributeType = attributeData.AttributeType;

                        if (!attributeType.IsGenericType
                            || attributeType.GetGenericTypeDefinition() is not
                            {
                                Namespace: "Sdl3Sharp.SourceGeneration",
                                Name: "NativeImportSymbolAttribute`1"
                                   or "NativeImportSymbolAttribute`2"
                                   or "NativeImportFunctionAttribute`1"
                                   or "NativeImportFunctionAttribute`2"
                            })
                        {
                            continue;
                        }

                        if (attributeData.ConstructorArguments is not [{ Value: string symbolName }, ..]
                            || string.IsNullOrWhiteSpace(symbolName))
                        {
                            symbolName = method.Name;
                        }

                        if (attributeType.GetGenericArguments() is not [_, var conditionType])
                        {
                            conditionType = null;
                        }

                        yield return (method, symbolName, conditionType);
                    }
                }
            }
		}

		static async ValueTask<PeFile> IPeFile<Managed, FileInfo>.CreateAsync(FileInfo file, CancellationToken cancellationToken)
		{
            var tpaPaths = AppContext.GetData("TRUSTED_PLATFORM_ASSEMBLIES") as string ?? throw new InvalidOperationException("TRUSTED_PLATFORM_ASSEMBLIES is not set.");
            var resolver = new PathAssemblyResolver([
                ..tpaPaths.Split(Path.PathSeparator, StringSplitOptions.RemoveEmptyEntries | StringSplitOptions.TrimEntries),
                file.FullName 
            ]);
            var metadataLoadContext = new MetadataLoadContext(resolver);
            var assembly = metadataLoadContext.LoadFromAssemblyPath(file.FullName);

			return new Managed(metadataLoadContext, assembly, file);
		}
	}

