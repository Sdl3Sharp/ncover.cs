file sealed class RuleEngine(params ReadOnlySpan<RuleEngine.Rule> rules)
{
    public delegate bool Rule(IContext context, string symbol, bool isExport, bool isImport, ref Severity severity, ref string? reason); // "Try"-pattern-style rules, in case a rule is just not applicable

    public enum Severity
    {
        Pass = 0,
        Slight = 1,
        Warn = 2,
        Error = 3
    }    

    public readonly struct Result(string symbol, bool isExport, bool isImport, Severity severity, IReadOnlyCollection<string> reasons)
    {
        public readonly string Symbol => symbol;
        public readonly bool IsExport => isExport;
        public readonly bool IsImport => isImport;
        public readonly Severity Severity => severity;
        public readonly IReadOnlyCollection<string> Reasons => reasons;

        public void Deconstruct(out string symbol, out bool isExport, out bool isImport, out Severity severity, out IReadOnlyCollection<string> reasons)
            => (symbol, isExport, isImport, severity, reasons) = (Symbol, IsExport, IsImport, Severity, Reasons);
    }

    public interface IContext
    {
        RuleEngine Engine { get; }
        IReadOnlySet<string> AllExports { get; }
        IReadOnlyDictionary<string, IReadOnlyCollection<(MethodInfo Method, SimpleSignature Signature, Type? ConditionType)>> AllImports { get; }

        Result CheckSymbol(string symbol, Severity initialSeverity = Severity.Pass);
    }

    private sealed class ContextImpl(RuleEngine engine, IReadOnlySet<string> allExports, IReadOnlyDictionary<string, IReadOnlyCollection<(MethodInfo Method, SimpleSignature Signature, Type? ConditionType)>> allImports) : IContext
    {
        public RuleEngine Engine => engine;
        public IReadOnlySet<string> AllExports => allExports;
        public IReadOnlyDictionary<string, IReadOnlyCollection<(MethodInfo Method, SimpleSignature Signature, Type? ConditionType)>> AllImports => allImports;

        public Result CheckSymbol(string symbol, Severity initialSeverity = Severity.Pass)
        {
            var isExport = AllExports.Contains(symbol);
            var isImport = AllImports.ContainsKey(symbol);
            List<string> reasons = [];            

            var severity = initialSeverity;
            foreach (var rule in engine.mRules)
            {
                var ruleSeverity = initialSeverity;
                var reason = (string?)null;
                if (rule(this, symbol, isExport, isImport, ref ruleSeverity, ref reason))
                {
                    if (ruleSeverity > severity) { severity = ruleSeverity; }

                    if (!string.IsNullOrWhiteSpace(reason)) { reasons.Add(reason); }
                }
            }
            
            return new Result(symbol, isExport, isImport, severity, reasons);
        }
    }

    private readonly FrozenSet<Rule> mRules = [..rules];

    public IContext CreateContext(IReadOnlySet<string> allExports, IReadOnlyDictionary<string, IReadOnlyCollection<(MethodInfo Method, SimpleSignature Signature, Type? ConditionType)>> allImports)
        => new ContextImpl(this, allExports, allImports);
}

