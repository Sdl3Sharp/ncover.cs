file static class StreamExtensions
{
    extension(Stream stream)
    {
        public async ValueTask ReadNullTerminatedAsync(ArrayBufferWriter<byte> writer, Memory<byte> chunkBuffer, CancellationToken cancellationToken = default)
        {
            while (true)
            {
                var bytesRead = await stream.ReadAsync(chunkBuffer, cancellationToken);

                if (bytesRead is 0) { throw new EndOfStreamException(); }

                var span = chunkBuffer.Span[..bytesRead];
                var nullIndex = span.IndexOf((byte)0);

                if (nullIndex is >= 0)
                {
                    writer.Write(span[..nullIndex]);
                    return;
                }
                
                writer.Write(span);
            }
        }
    }
}

