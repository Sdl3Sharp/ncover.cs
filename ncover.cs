var minSeverityOption = new Option<Severity>( "--min-severity", "-m")
{
    Description = "Minimum severity level of the report. Reports with a severity level below this value will be ignored.",
    Arity = ArgumentArity.ExactlyOne,
    DefaultValueFactory = static _ => Severity.All,
    CustomParser = static result =>
    {
        if (result.Tokens is not [var token])
        {
            result.AddError($"Expected exactly one argument for --min-severity.");
            return Severity.All;
        }

        switch (token.Value)
        {
            case var value when nameof(Severity.All).StartsWith(value, StringComparison.OrdinalIgnoreCase):    return Severity.All;
            case var value when nameof(Severity.Slight).StartsWith(value, StringComparison.OrdinalIgnoreCase): return Severity.Slight;
            case var value when nameof(Severity.Warn).StartsWith(value, StringComparison.OrdinalIgnoreCase):   return Severity.Warn;
            case var value when nameof(Severity.Error).StartsWith(value, StringComparison.OrdinalIgnoreCase):  return Severity.Error;
            case var value when nameof(Severity.None).StartsWith(value, StringComparison.OrdinalIgnoreCase):   return Severity.None;

            case var value when int.TryParse(value, out var intValue)
                                && unchecked((Severity)intValue) is var severity
                                && severity is >= Severity.All and <= Severity.None:
                                    return severity;
        }
        
        result.AddError($"Invalid severity level: {token.Value}. Must be one of: {nameof(Severity.All)}, {nameof(Severity.Slight)}, {nameof(Severity.Warn)}, {nameof(Severity.Error)}, {nameof(Severity.None)}, or an integer between 0 and 4.");
        return Severity.All;
    }
};

