file static class SpanExtensions
{
    public readonly ref struct SplitWhitespacesEnumerable(ReadOnlySpan<char> span)
    {
        public ref struct Enumerator(SplitWhitespacesEnumerable enumerable)
        {
            private readonly SplitWhitespacesEnumerable mEnumerable = enumerable;
            private int mStart = 0;

            public Range Current { readonly get; private set; } = default;

            public bool MoveNext()
            {
                var span = mEnumerable.mSpan;

                var start = mStart;
                while (start < span.Length && char.IsWhiteSpace(span[start])) { start++; }
                var end = start;
                while (end < span.Length && !char.IsWhiteSpace(span[end])) { end++; }

                if (start < end)
                {
                    Current = start..end;
                    mStart = end;
                    return true;
                }
                else
                {
                    Current = default;
                    return false;
                }
            }

            public void Reset() { mStart = 0; Current = default; }
        }

        private readonly ReadOnlySpan<char> mSpan = span;

        public readonly Enumerator GetEnumerator() => new(this);
    }

    extension(ReadOnlySpan<char> span)
    {
        public SplitWhitespacesEnumerable SplitWhitespaces() => new(span);
    }

    extension(Span<char> span)
    {
