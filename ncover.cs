// Rules:
var rules = new RuleEngine(
    // R1: Unconditional import of a non-existing export -> error
    static (context, symbol, isExport, isImport, ref severity, ref reason) =>
    {
        if (!isExport && isImport
            && context.AllImports.TryGetValue(symbol, out var imports)
            && imports.Any(static i => i.ConditionType is null))
        {
            severity = RuleEngine.Severity.Error;
            reason = "Symbol is unconditionally imported but not exported by the native library.";
            return true;
        }
        return false;
    },
    // R2: Existing export has no imports -> warn
    static (_, _, isExport, isImport, ref severity, ref reason) =>
    {
        if (isExport && !isImport)
        {
            severity = RuleEngine.Severity.Warn;
            reason = "Symbol is exported by the native library but not imported by the managed assembly.";
            return true;
        }
        return false;
    },
    // R3: Multiple unconditional imports share a signature -> slight
    static (context, symbol, _, isImport, ref severity, ref reason) =>
    {
        if (isImport
            && context.AllImports.TryGetValue(symbol, out var imports)
            && imports
                .Where(static i => i.ConditionType is null)
                .GroupBy(static i => i.Signature)
                .Any(static g => g.Count() is > 1))
        {
            severity = RuleEngine.Severity.Slight;
            reason = "Symbol is unconditionally imported more than once with the same signature. The duplicate imports might be redundant.";
            return true;
        }
        return false;
    },
    // R4: Multiple conditional imports under the same condition share a signature -> slight
    static (context, symbol, _, isImport, ref severity, ref reason) =>
    {
        if (isImport
            && context.AllImports.TryGetValue(symbol, out var imports)
            && imports
                .Where(static i => i.ConditionType is not null)
                .GroupBy(
                    keySelector: static i => i.ConditionType,
                    resultSelector: static (c, i) => i
                        .GroupBy(static i => i.Signature)
                        .Any(static g => g.Count() is > 1))
                .Any(static b => b))
        {
            severity = RuleEngine.Severity.Slight;
            reason = "Symbol is conditionally imported more than once under the same condition with the same signature. The duplicate imports might be redundant.";
            return true;
        }
        return false;
    },
    // R5: An unconditional import shares a signature with a conditional import -> warn
    static (context, symbol, _, isImport, ref severity, ref reason) =>
    {
        if (isImport
            && context.AllImports.TryGetValue(symbol, out var imports)
            && imports
                .Where(static i => i.ConditionType is null)
                .Select(static i => i.Signature)
                .Intersect(imports
                    .Where(static i => i.ConditionType is not null)
                    .Select(static i => i.Signature))
                .Any())
        {
            severity = RuleEngine.Severity.Warn;
            reason = "Symbol has both an unconditional and a conditional import with the same signature. The conditional import might be redundant.";
            return true;
        }
        return false;
    },
    // R6: Conditional imports under different conditions share a signature -> slight
    static (context, symbol, isExport, isImport, ref severity, ref reason) =>
    {
        if (isImport
            && context.AllImports.TryGetValue(symbol, out var imports)
            && imports
                .Where(static i => i.ConditionType is not null)
                .GroupBy(
                    keySelector: static i => i.Signature,
                    resultSelector: static (s, i) => i.DistinctBy(static i => i.ConditionType).Count() is > 1)
                .Any(static b => b))
        {
            severity = RuleEngine.Severity.Slight;
            reason = "Symbol is imported conditionally under different conditions but with the same signature. The conditions could be merged into a single import.";
            return true;
        }
        return false;
    }
);

rootCommand.SetAction(async (result, cancellationToken) =>
{
    var errorOptions = default((Verbosity verbosity, OutputFormat outputFormat)?);

    try
    {
        var excludeFile  = result.GetValue(excludeFileOption);
        var excludes     = result.GetValue(excludeOption);
        var minSeverity  = result.GetValue(minSeverityOption);
        var slightAsWarn = result.GetValue(slightAsWarnOption);
        var warnAsError  = result.GetValue(warnAsErrorOption);
        var jsonOutput   = result.GetValue(jsonOutputOption);
        var noUnicode    = result.GetValue(noUnicodeOption);
        var noAnsi       = result.GetValue(noAnsiOption);
        var verbosity    = result.GetValue(verbosityOption);
        var pretty       = result.GetValue(prettyOption);
        var package      = result.GetValue(packageOption);
        var preferX64    = result.GetValue(preferX64Option);
        var nugetSource  = result.GetValue(nugetSourceOption);        

        var outputFormat = new OutputFormat(useAnsi: !noAnsi, useUnicode: !noUnicode);

        errorOptions = (verbosity, outputFormat);

        var output = Output.Create(verbosity);

        await output.WriteLineVerboseAsync(() => outputFormat.HLine, cancellationToken); 

        NuGetPackagesResolver? nuGetPackages;
        FileInfo firstPe, secondPe;
        if (package is not null)
        {
            nuGetPackages = await NuGetPackagesResolver.LookupAndDownloadAsync(
                packageTerm: package,
                preferX64: preferX64,
                nuGetSource: nugetSource,

                onBeginPackageLookupAsync: (packageId, versionRange, cancellationToken) => output.WriteLineVerboseAsync(() => $"Looking up meta package \"{packageId}\"{(versionRange is not null ? $" with version range {versionRange}" : string.Empty)}...", cancellationToken),
                onEndPackageLookupAsync: (packageIdentity, cancellationToken) => output.WriteLineVerboseAsync(() => $"Found meta package \"{packageIdentity}\".", cancellationToken),
                onBeginDependencyPackagesLookupAsync: (packageIdentity, cancellationToken) => output.WriteLineVerboseAsync(() => $"Looking up dependency packages for meta package \"{packageIdentity}\"...", cancellationToken),
                onEndDependencyPackagesLookupAsync: (ridPackageIdentity, rid, corePackageIdentity, cancellationToken) => output.WriteLineVerboseAsync(() => $"Found RID-specific dependency package \"{ridPackageIdentity}\" for RID \"{rid}\" with core dependency \"{corePackageIdentity}\".", cancellationToken),
                onBeginDownloadRidPackageAsync: (ridPackageIdentity, rid, cancellationToken) => output.WriteLineVerboseAsync(() => $"Downloading RID-specific package \"{ridPackageIdentity}\" for RID \"{rid}\"...", cancellationToken),
                onEndDownloadRidPackageAsync: (ridPackageIdentity, rid, cancellationToken) => output.WriteLineVerboseAsync(() => $"Finished downloading RID-specific package \"{ridPackageIdentity}\" for RID \"{rid}\".", cancellationToken),
                onBeginExtractNativeLibraryAsync: (ridPackageIdentity, rid, cancellationToken) => output.WriteLineVerboseAsync(() => $"Extracting native library from RID-specific package \"{ridPackageIdentity}\" for RID \"{rid}\"...", cancellationToken),
                onEndExtractNativeLibraryAsync: (ridPackageIdentity, rid, nativeLibraryFileName, cancellationToken) => output.WriteLineVerboseAsync(() => $"Finished extracting native library \"{nativeLibraryFileName}\" from RID-specific package \"{ridPackageIdentity}\" for RID \"{rid}\".", cancellationToken),
                onBeginDownloadCorePackageAsync: (corePackageIdentity, cancellationToken) => output.WriteLineVerboseAsync(() => $"Downloading core package \"{corePackageIdentity}\"...", cancellationToken),
                onEndDownloadCorePackageAsync: (corePackageIdentity, cancellationToken) => output.WriteLineVerboseAsync(() => $"Finished downloading core package \"{corePackageIdentity}\".", cancellationToken),
                onBeginExtractManagedLibraryAsync: (corePackageIdentity, cancellationToken) => output.WriteLineVerboseAsync(() => $"Extracting managed library from core package \"{corePackageIdentity}\"...", cancellationToken),
                onEndExtractManagedLibraryAsync: (corePackageIdentity, managedLibraryFileName, cancellationToken) => output.WriteLineVerboseAsync(() => $"Finished extracting managed library \"{managedLibraryFileName}\" from core package \"{corePackageIdentity}\".", cancellationToken),

                cancellationToken: cancellationToken
            );

            if (nuGetPackages is null) { return -1; /* we already reported errors */ }

            firstPe = nuGetPackages.ManagedLibraryFile;
            secondPe = nuGetPackages.NativeLibraryFile;

        }
        else
        {     
            nuGetPackages = null;

            firstPe = result.GetRequiredValue(firstPeArgurment)!;
            secondPe = result.GetRequiredValue(secondPeArgument)!;
        }

        try
        {
            HashSet<string> excludedSymbols = new(StringComparer.Ordinal);

            if (excludeFile is not null)
            {
                await using var excludeStream = excludeFile.OpenRead();
                using var excludeReader = new StreamReader(excludeStream);

                for (var line = await excludeReader.ReadLineAsync(cancellationToken);
                    line is not null;
                    line = await excludeReader.ReadLineAsync(cancellationToken))
                {
                    var span = line.AsSpan();
                    if (span.IndexOf('#') is var commentIndex && commentIndex is >= 0) { span = span[..commentIndex]; }
                    foreach (var range in span.SplitWhitespaces()) { excludedSymbols.Add(span[range].ToString()); }
                }
            }

            if (excludes is not null)
            {
                foreach (var exclude in excludes)
                {            
                    var span = exclude.AsSpan();
                    foreach (var range in span.SplitWhitespaces()) { excludedSymbols.Add(span[range].ToString()); }
                }
            }

            await output.WriteLineVerboseAsync(() => $"Opening PE files \"{firstPe.FullName}\" and \"{secondPe.FullName}\"...", cancellationToken);

            await using var firstPeFile = await PeFile.OpenAsync(firstPe, cancellationToken);
            await using var secondPeFile = await PeFile.OpenAsync(secondPe, cancellationToken);

            var (nativePe, managedPe) = (firstPeFile, secondPeFile) switch
            {
                (PeFile.Native native, PeFile.Managed managed) => (native, managed),
                (PeFile.Managed managed, PeFile.Native native) => (native, managed),

                _ => throw new InvalidOperationException($"One of the provided PE files must be a native PE and the other must be a .NET assembly.")
            };

            await output.WriteLineVerboseAsync(() => $"Successfully opened PE files. \"{nativePe.File.FullName}\" is the native PE and \"{managedPe.File.FullName}\" is the .NET assembly.", cancellationToken);
            
            await output.WriteLineVerboseAsync(() => outputFormat.HLine, cancellationToken);            
            await output.WriteLineAsync(cancellationToken);

            var exportedSymbols = await nativePe.EnumerateExportedSymbolsAsync(cancellationToken)
                .Where(symbol => !excludedSymbols.Contains(symbol))
                .ToHashSetAsync(StringComparer.Ordinal, cancellationToken);

            var importedSymbols = managedPe.EnumerateImportedSymbols()
                .Where(symbol => !excludedSymbols.Contains(symbol.SymbolName))
                .GroupBy(symbol => symbol.SymbolName, symbol => (symbol.Method, symbol.Method.GetSimpleSignature(), symbol.ConditionType), StringComparer.Ordinal)
                .ToDictionary(group => group.Key, group => (IReadOnlyCollection<(MethodInfo Method, SimpleSignature Signature, Type? ConditionType)>)group.ToList(), StringComparer.Ordinal);

            var rulesContext = rules.CreateContext(exportedSymbols, importedSymbols);

            int passes = 0, warnings = 0, errors = 0, exportsImported = 0;
            var reportEntries = ImmutableDictionary.CreateBuilder<string, ReportData.EntryData>(StringComparer.Ordinal);            
            var builder = new StringBuilder();
            foreach (var symbol in exportedSymbols.Union(importedSymbols.Keys, StringComparer.Ordinal).OrderBy(static s => s, StringComparer.Ordinal))
            {
                var (_, isExport, isImport, severity, reasons) = rulesContext.CheckSymbol(symbol);

                if (isExport && isImport) { exportsImported++; }

                var effectiveSeverity = severity switch
                {
                    RuleEngine.Severity.Pass          => ReportData.EntryData.EffectiveSeverity.Pass,
                    RuleEngine.Severity.Slight        => slightAsWarn
                        ? warnAsError
                            ? ReportData.EntryData.EffectiveSeverity.Error
                            : ReportData.EntryData.EffectiveSeverity.Warn
                        : ReportData.EntryData.EffectiveSeverity.Pass,
                    RuleEngine.Severity.Warn => warnAsError
                        ? ReportData.EntryData.EffectiveSeverity.Error
                        : ReportData.EntryData.EffectiveSeverity.Warn,
                    _ /* RuleEngine.Severity.Error */ => ReportData.EntryData.EffectiveSeverity.Error
                };

                switch (effectiveSeverity)
                {
                    case ReportData.EntryData.EffectiveSeverity.Pass: passes++; break;
                    case ReportData.EntryData.EffectiveSeverity.Warn: warnings++; break;
                    case ReportData.EntryData.EffectiveSeverity.Error: errors++; break;
                }

                if (minSeverity switch
                {
                    Severity.None   => true,
                    Severity.Error  => severity is >= RuleEngine.Severity.Error,
                    Severity.Warn   => severity is >= RuleEngine.Severity.Warn,
                    Severity.Slight => severity is >= RuleEngine.Severity.Slight,
                    _               => false
                }) { continue; }                

                reportEntries.Add(symbol, new ReportData.EntryData(
                    Severity: effectiveSeverity,
                    Reasons: reasons is { Count: > 0 }
                        ? [..reasons]
                        : null,
                    Imports:  isImport && importedSymbols.TryGetValue(symbol, out var imports)
                        ? [..imports.Select(i => new ReportData.EntryData.ImportData(
                            MethodName: builder.Clear().AppendQualifiedCSharpTypeName(i.Method.DeclaringType).Append('.').Append(i.Method.Name).ToString(),
                            ConditionTypeName: i.ConditionType is not null
                                ? builder.Clear().AppendQualifiedCSharpTypeName(i.ConditionType).ToString()
                                : null 
                        ))]
                        : []
                ));
            }

            var managedAssemblyName = managedPe.Assembly.GetName();            
            var nativeInformation = await nativePe.GetInformationAsync(cancellationToken);

            var report = new ReportData(
                ManagedLibrary: new ReportData.PeData(
                    FileName: managedPe.File.Name,
                    Name: managedPe.Assembly
                        .GetCustomAttributesData()
                        .FirstOrDefault(static a => a.AttributeType.FullName is "System.Reflection.AssemblyProductAttribute")?
                        .ConstructorArguments
                        .FirstOrDefault()
                        .Value as string
                        ?? managedAssemblyName.Name,
                    Version: managedPe.Assembly
                        .GetCustomAttributesData()
                        .FirstOrDefault(static a => a.AttributeType.FullName is "System.Reflection.AssemblyInformationalVersionAttribute")?
                        .ConstructorArguments
                        .FirstOrDefault()
                        .Value as string
                        ?? managedAssemblyName.Version?.ToString(),
                    SourcePackage: nuGetPackages is not null
                        ? new ReportData.PeData.PackageData(
                            Id: nuGetPackages.CorePackage.Id,
                            Version: nuGetPackages.CorePackage.HasVersion
                                ? nuGetPackages.CorePackage.Version.ToString()
                                : null
                        )
                        : null
                ),
                NativeLibrary: new ReportData.PeData(
                    FileName: nativePe.File.Name,
                    Name: nativeInformation.ProductName ?? nativeInformation.FileDescription,
                    Version: nativeInformation.ProductVersion ?? nativeInformation.FixedProductVersion?.ToString() ?? nativeInformation.FileVersion ?? nativeInformation.FixedFileVersion?.ToString(),
                    SourcePackage: nuGetPackages is not null
                        ? new ReportData.PeData.PackageData(
                            Id: nuGetPackages.RidPackage.Id,
                            Version: nuGetPackages.RidPackage.HasVersion
                                ? nuGetPackages.RidPackage.Version.ToString()
                                : null
                        )
                        : null
                ),
                PassEntriesCount: passes,
                WarningEntriesCount: warnings,
                ErrorEntriesCount: errors,
                ImportedSymbolsCount: exportsImported,
                ExportedSymbolsCount: exportedSymbols.Count,
                CoverageRatio: exportsImported / (exportedSymbols.Count switch { <= 0 => 1.0, var count => (double)count }),
                Entries: reportEntries.ToImmutable(),
                NuGetSource: nuGetPackages?.NuGetSource
            );

            if (verbosity is Verbosity.Json)
            {
                await using var @out = OpenStandardOutput();
                await JsonSerializer.SerializeAsync(@out, report, 
                    pretty
                        ? ReportDataPrettySerializerContext.Default.ReportData
                        : ReportDataSerializerContext.Default.ReportData,
                    cancellationToken
                );
            }
            else
            {
                foreach (var (symbol, (severity, reasons, imports)) in report.Entries.OrderBy(p => p.Key, StringComparer.Ordinal))
                {
                    await output.WriteLineAsync(() =>
                        builder.Clear()
                            .Append(severity switch
                            {
                                ReportData.EntryData.EffectiveSeverity.Error        => $"{outputFormat.Error} {outputFormat.Red}",
                                ReportData.EntryData.EffectiveSeverity.Warn         => $"{outputFormat.Warning} {outputFormat.Yellow}",
                                _ /* ReportData.EntryData.EffectiveSeverity.Pass */ => $"{outputFormat.Success} {outputFormat.Green}"
                            })
                            .Append(outputFormat.Underlined)
                            .Append(outputFormat.Bold)
                            .Append(symbol)
                            .Append(outputFormat.Reset),
                        cancellationToken
                    );

                    for (var i = 0; i < imports.Length; i++)
                    {
                        var isLast = i == imports.Length - 1;
                        var (method, condition) = imports[i];

                        await output.WriteLineAsync(() =>
                            builder.Clear()
                                .Append(outputFormat.Intendation)
                                .Append(outputFormat.Dim)
                                .Append(outputFormat.BoxV)
                                .Append(outputFormat.Reset),
                            cancellationToken
                        );                        

                        await output.WriteLineAsync(() =>
                        {
                            var methodSpan = method.AsSpan();
                            var prefixLength = methodSpan.LastIndexOf('.') + 1;

                            return builder.Clear()
                                .Append(outputFormat.Intendation)
                                .Append(outputFormat.Dim)
                                .Append(isLast? outputFormat.BoxUR : outputFormat.BoxVR).Append(outputFormat.BoxH).Append(outputFormat.RightPointer)
                                .Append(outputFormat.Reset)
                                .Append(' ')
                                .Append(methodSpan[..prefixLength])
                                .Append(outputFormat.Underlined)
                                .Append(outputFormat.Bold)
                                .Append(methodSpan[prefixLength..])
