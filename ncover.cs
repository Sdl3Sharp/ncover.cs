		public async IAsyncEnumerable<string> EnumerateExportedSymbolsAsync([EnumeratorCancellation] CancellationToken cancellationToken = default)
		{
            const int exportDirectoryNumberOfNamesOffset = 24,
                      exportDirectoryAddressOfNamesOffset = 32;

            ObjectDisposedException.ThrowIf(mArgs.Stream is null, this);

            if (mArgs.ExportDirectoryEntryRva is 0 || mArgs.ExportDirectoryEntrySize is 0) { yield break; }
            
            // Map export directory RVA -> file offset
            if (!TryRvaToFileOffset(mArgs.ExportDirectoryEntryRva, out var exportDirectoryFileOffset)) { throw new InvalidDataException("Export directory RVA could not be mapped to a file offset."); }

            // Image Export Directory
            mArgs.Stream.Seek(exportDirectoryFileOffset + exportDirectoryNumberOfNamesOffset, SeekOrigin.Begin);
            var numberOfNames = await mArgs.Stream.ReadLittleEndianAsync<uint>(cancellationToken);

            mArgs.Stream.Seek(exportDirectoryFileOffset + exportDirectoryAddressOfNamesOffset, SeekOrigin.Begin);
            var addressOfNamesRva = await mArgs.Stream.ReadLittleEndianAsync<uint>(cancellationToken);

            if (numberOfNames is 0 || addressOfNamesRva is 0) { yield break; }

            if (!TryRvaToFileOffset(addressOfNamesRva, out var nameRvaFileOffset)) { throw new InvalidDataException("AddressOfNames RVA could not be mapped to a file offset.");}

            // Read name RVA array
            var nameArrayRvaMemory = GC.AllocateUninitializedArray<byte>(checked((int)(numberOfNames * sizeof(uint)))).AsMemory();
            
            mArgs.Stream.Seek(nameRvaFileOffset, SeekOrigin.Begin);
            await mArgs.Stream.ReadExactlyAsync(nameArrayRvaMemory, cancellationToken);

            // Read names
            var nameWriter = new ArrayBufferWriter<byte>();
            for (var i = 0; i < numberOfNames; i++)
            {
                var nameRva = MemoryMarshal.Read<uint>(nameArrayRvaMemory.Span);
                if (!BitConverter.IsLittleEndian) { nameRva = BinaryPrimitives.ReverseEndianness(nameRva); }

                nameArrayRvaMemory = nameArrayRvaMemory[sizeof(uint)..];

                if (nameRva is 0) { continue; }

                if (!TryRvaToFileOffset(nameRva, out var nameFileOffset)) { continue; }

                mArgs.Stream.Seek(nameFileOffset, SeekOrigin.Begin);
                await mArgs.Stream.ReadNullTerminatedAsync(nameWriter, mArgs.Buffer, cancellationToken);

                yield return Encoding.UTF8.GetString(nameWriter.WrittenSpan);

                nameWriter.Clear();
            }
		}

        public async ValueTask<Information> GetInformationAsync(CancellationToken cancellationToken = default)
        {
            const uint rtVersion = 16;
            const int namedCountOffset = 12,
                      resourceDirectoryHeaderSize = 16,
                      resourceDirectoryEntrySize = 8;
            
            ObjectDisposedException.ThrowIf(mArgs.Stream is null, this);

            if (mArgs.ResourceDirectoryEntryRva is 0 || mArgs.ResourceDirectoryEntrySize is 0) { return default; }

            // Map resource directory RVA -> file offset
            if (!TryRvaToFileOffset(mArgs.ResourceDirectoryEntryRva, out var resourceDirectoryFileOffset)) { throw new InvalidDataException("Resource directory RVA could not be mapped to a file offset."); }

            // Level 1: Find RT_VERSION entry
            mArgs.Stream.Seek(resourceDirectoryFileOffset + namedCountOffset, SeekOrigin.Begin);
            var namedCount = await mArgs.Stream.ReadLittleEndianAsync<ushort>(cancellationToken);
            var idCount = await mArgs.Stream.ReadLittleEndianAsync<ushort>(cancellationToken);

            mArgs.Stream.Seek(resourceDirectoryFileOffset + resourceDirectoryHeaderSize + (long)namedCount * resourceDirectoryEntrySize, SeekOrigin.Begin); // RT_VERSION is an ID entry, so we skip over all named entries

            uint level2RelativeOffset;
            for (var i = 0; true; i++)
            {
                var id = await mArgs.Stream.ReadLittleEndianAsync<uint>(cancellationToken);
                level2RelativeOffset = await mArgs.Stream.ReadLittleEndianAsync<uint>(cancellationToken);

                if (id is rtVersion)
                {
                    if ((level2RelativeOffset & 0x8000_0000u) is 0) { throw new InvalidDataException("Unexpected format of resource directory entry for RT_VERSION. Expected a subdirectory, but got a data entry."); }
                    level2RelativeOffset &= 0x7FFF_FFFFu;

                    break;
                }

                if (i >= idCount) { return default; }
            }

            // Map level 2 RVA -> file offset
            if (!TryRvaToFileOffset(mArgs.ResourceDirectoryEntryRva + level2RelativeOffset, out var level2FileOffset)) { throw new InvalidDataException("Level 2 resource directory RVA could not be mapped to a file offset."); }

            // Level 2: Take the first entry
            mArgs.Stream.Seek(level2FileOffset + namedCountOffset, SeekOrigin.Begin);
            var level2NamedCount = await mArgs.Stream.ReadLittleEndianAsync<ushort>(cancellationToken);
            var level2IdCount = await mArgs.Stream.ReadLittleEndianAsync<ushort>(cancellationToken);

            if (level2NamedCount + level2IdCount is 0) { return default; }

            mArgs.Stream.Seek(level2FileOffset + resourceDirectoryHeaderSize + sizeof(uint), SeekOrigin.Begin); // "+ sizeof(uint)" to skip over the name/id, we don't care about that
            var level3RelativeOffset = await mArgs.Stream.ReadLittleEndianAsync<uint>(cancellationToken);
            if ((level3RelativeOffset & 0x8000_0000u) is 0) { throw new InvalidDataException("Unexpected format of level 2 resource directory entry for RT_VERSION. Expected a subdirectory, but got a data entry."); }
            level3RelativeOffset &= 0x7FFF_FFFFu;

            // Map level 3 RVA -> file offset
            if (!TryRvaToFileOffset(mArgs.ResourceDirectoryEntryRva + level3RelativeOffset, out var level3FileOffset)) { throw new InvalidDataException("Level 3 resource directory RVA could not be mapped to a file offset."); }

            // Level 3: Take the first language entry
            mArgs.Stream.Seek(level3FileOffset + namedCountOffset, SeekOrigin.Begin);
            var level3NamedCount = await mArgs.Stream.ReadLittleEndianAsync<ushort>(cancellationToken);
            var level3IdCount = await mArgs.Stream.ReadLittleEndianAsync<ushort>(cancellationToken);

            if (level3NamedCount + level3IdCount is 0) { return default; }

            mArgs.Stream.Seek(level3FileOffset + resourceDirectoryHeaderSize + sizeof(uint), SeekOrigin.Begin); // "+ sizeof(uint)" to skip over the language id, we don't care about that
            var dataEntryRelativeOffset = await mArgs.Stream.ReadLittleEndianAsync<uint>(cancellationToken);
            if ((dataEntryRelativeOffset & 0x8000_0000u) is not 0) { throw new InvalidDataException("Unexpected format of level 3 resource directory entry for RT_VERSION. Expected a data entry, but got a subdirectory."); }

            // Map data entry RVA -> file offset
            if (!TryRvaToFileOffset(mArgs.ResourceDirectoryEntryRva + dataEntryRelativeOffset, out var dataEntryFileOffset)) { throw new InvalidDataException("Data entry RVA for RT_VERSION could not be mapped to a file offset."); }

            // Image Resource Data Entry
            mArgs.Stream.Seek(dataEntryFileOffset, SeekOrigin.Begin);
            var dataRva = await mArgs.Stream.ReadLittleEndianAsync<uint>(cancellationToken);
            var dataSize = await mArgs.Stream.ReadLittleEndianAsync<int>(cancellationToken);
            if (dataRva is 0 || dataSize is 0) { return default; }

            // Map data RVA -> file offset
            if (!TryRvaToFileOffset(dataRva, out var dataFileOffset)) { throw new InvalidDataException("Data RVA for RT_VERSION could not be mapped to a file offset."); }

            // Read raw VS_VERSION_INFO
            var versionData = GC.AllocateUninitializedArray<byte>(dataSize);
            mArgs.Stream.Seek(dataFileOffset, SeekOrigin.Begin);
            await mArgs.Stream.ReadExactlyAsync(versionData, cancellationToken);

            // Parse VS_VERSION_INFO
            var versionDataSpan = versionData.AsSpan();
            if (versionDataSpan.Length < 6) { throw new InvalidDataException("VS_VERSION_INFO data is too small to contain mandatory fields."); }

            static int alignUp(int value, int alignment) => unchecked((value + (alignment - 1)) & ~(alignment - 1));

            var wValueLength = versionDataSpan[2..].ReadLittleEndian<ushort>(out _);
            var fixedFileInfoOffset = alignUp(6 + "VS_VERSION_INFO\0".Length * 2, 4); // header (6 bytes) + "VS_VERSION_INFO\0" (32 bytes, UTF-16), aligned up to 4 bytes (should be 40 bytes in total)

            Version? fixedFileVersion = null, fixedProductVersion = null;
            if (wValueLength is >= 52 && fixedFileInfoOffset + 52 <= versionDataSpan.Length)
            {
                var fixedFileInfoSpan = versionDataSpan[fixedFileInfoOffset..];

                var signature = fixedFileInfoSpan.ReadLittleEndian<uint>(out fixedFileInfoSpan);
                if (signature is not 0xFEEF04BDu) { throw new InvalidDataException("Invalid signature in VS_FIXEDFILEINFO. Expected 0xFEEF04BD."); }

                _ = fixedFileInfoSpan.ReadLittleEndian<uint>(out fixedFileInfoSpan); // struct version, we don't care about this

                var fileVersionMS = fixedFileInfoSpan.ReadLittleEndian<uint>(out fixedFileInfoSpan);
                var fileVersionLS = fixedFileInfoSpan.ReadLittleEndian<uint>(out fixedFileInfoSpan);
                var productVersionMS = fixedFileInfoSpan.ReadLittleEndian<uint>(out fixedFileInfoSpan);
                var productVersionLS = fixedFileInfoSpan.ReadLittleEndian<uint>(out fixedFileInfoSpan);

                fixedFileVersion = new Version(
                    major:    unchecked((int)(fileVersionMS >> 16)),
                    minor:    unchecked((int)(fileVersionMS & 0xFFFFu)),
                    build:    unchecked((int)(fileVersionLS >> 16)),
                    revision: unchecked((int)(fileVersionLS & 0xFFFFu))
                );
                fixedProductVersion = new Version(
                    major:    unchecked((int)(productVersionMS >> 16)),
                    minor:    unchecked((int)(productVersionMS & 0xFFFFu)),
                    build:    unchecked((int)(productVersionLS >> 16)),
                    revision: unchecked((int)(productVersionLS & 0xFFFFu))
                );
            }

            var blockOffset = alignUp(fixedFileInfoOffset + wValueLength, 4);

            string? fileDescription = null, fileVersion = null, productName = null, productVersion = null;
            while (blockOffset + 6 <= versionDataSpan.Length)
            {
                var blockLength = versionDataSpan[blockOffset..].ReadLittleEndian<ushort>(out _);
                if (blockLength is 0 || blockOffset + blockLength > versionDataSpan.Length) { break; }

                var blockKey = versionDataSpan[(blockOffset + 6)..].ReadNullTerminatedUtf16LittleEndian(out _);
                var blockKeyEnd = alignUp(6 + (blockKey.Length + 1) * 2, 4); // header (6 bytes) + block key (UTF-16), aligned up to 4 bytes

                if (blockKey is "StringFileInfo")
                {
                    var stringFileEnd = blockOffset + blockLength;

                    var stringTableOffset = blockOffset + blockKeyEnd;
                    while (stringTableOffset + 6 <= stringFileEnd)
                    {
                        var stringTableLength = versionDataSpan[stringTableOffset..].ReadLittleEndian<ushort>(out _);
                        if (stringTableLength is 0 || stringTableOffset + stringTableLength > stringFileEnd) { break; }

                        var stringTableKey = versionDataSpan[(stringTableOffset + 6)..].ReadNullTerminatedUtf16LittleEndian(out _);
                        var stringOffset = stringTableOffset + alignUp(6 + (stringTableKey.Length + 1) * 2, 4); // header (6 bytes) + string table key (UTF-16), aligned up to 4 bytes
                        var stringEnd = stringTableOffset + stringTableLength;

                        while (stringOffset + 6 <= stringEnd)
                        {
                            var stringLength = versionDataSpan[stringOffset..].ReadLittleEndian<ushort>(out _);
                            if (stringLength is 0 || stringOffset + stringLength > stringEnd) { break; }

                            var stringValueLength = versionDataSpan[(stringOffset + 2)..].ReadLittleEndian<ushort>(out _); // in wchars
                            var stringKey = versionDataSpan[(stringOffset + 6)..].ReadNullTerminatedUtf16LittleEndian(out _);
                            var stringValueOffset = stringOffset + alignUp(6 + (stringKey.Length + 1) * 2, 4); // header (6 bytes) + string key (UTF-16), aligned up to 4 bytes
                            var stringValueEnd = stringValueOffset + stringValueLength * 2;

                            if (stringValueLength is > 0 && stringValueEnd <= versionDataSpan.Length)
                            {
                                var stringValue = versionDataSpan[stringValueOffset..stringValueEnd].ReadNullTerminatedUtf16LittleEndian(out _);
                                switch (stringKey)
                                {
                                    case "FileDescription": fileDescription = stringValue; break;
                                    case "FileVersion":     fileVersion     = stringValue; break;
                                    case "ProductName":     productName     = stringValue; break;
                                    case "ProductVersion":  productVersion  = stringValue; break;
                                }

                                if (fileDescription is not null && fileVersion is not null && productName is not null && productVersion is not null) { goto End; }
                            }

                            stringOffset += alignUp(stringLength, 4);                            
                        }

                        stringTableOffset += alignUp(stringTableLength, 4);
                    }
                }

                blockOffset += alignUp(blockLength, 4);
            }

        End:
            return new Information(fixedFileVersion, fixedProductVersion, fileDescription, fileVersion, productName, productVersion);
        }

        private bool TryRvaToFileOffset(uint rva, out uint fileOffset)
        {
            if (rva is 0)
            {
                fileOffset = 0;
                return false;
            }

            if (rva < mArgs.SizeOfHeaders)
            {
                fileOffset = rva;
                return true;
            }

            foreach (var (VirtualSize, VirtualAddress, SizeOfRawData, PointerToRawData) in mSections)
            {
                var extent = uint.Max(SizeOfRawData, VirtualSize);

                if (extent is 0) { continue; }

                if (rva >= VirtualAddress && (ulong)rva < (ulong)VirtualAddress + (ulong)extent)
                {
                    fileOffset = checked((uint)((ulong)PointerToRawData + (ulong)(rva - VirtualAddress)));
                    return true;
                }
            }

            fileOffset = 0;
            return false;
        }

		static async ValueTask<PeFile> IPeFile<Native, NativeArgs>.CreateAsync(NativeArgs args, CancellationToken cancellationToken)
        {    
            const int sectionHeaderSize = 40,
                      sectionHeaderVirtualSizeOffset = 8;

            if (args.Stream is null) { throw new ArgumentException("Stream should not be null. This is very unexpected.", nameof(args)); }

            var sectionHeaderOffset = args.SectionTableOffset;
            var sections = GC.AllocateUninitializedArray<SectionHeader>(args.NumberOfSections);

            for (var i = 0; i < args.NumberOfSections; i++)
            {
                args.Stream.Seek(sectionHeaderOffset + sectionHeaderVirtualSizeOffset, SeekOrigin.Begin);

                sections[i] = new(
                    VirtualSize: await args.Stream.ReadLittleEndianAsync<uint>(cancellationToken),
                    VirtualAddress: await args.Stream.ReadLittleEndianAsync<uint>(cancellationToken),
                    SizeOfRawData: await args.Stream.ReadLittleEndianAsync<uint>(cancellationToken),
                    PointerToRawData: await args.Stream.ReadLittleEndianAsync<uint>(cancellationToken)
                );

                sectionHeaderOffset += sectionHeaderSize;
            }

            return new Native(args, sections);
        }
	}

