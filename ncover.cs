                                    .Append(outputFormat.BoxUR).Append(outputFormat.BoxH).Append(outputFormat.RightPointer)
                                    .Append(outputFormat.Blue)
                                    .Append(" Condition: ")
                                    .Append(outputFormat.Reset)
                                    .Append(outputFormat.Blue)
                                    .Append(condition)
                                    .Append(outputFormat.Reset)
                                    .ToString();
                            }, cancellationToken);
                        }
                    }

                    if (reasons is { Length: > 0 } nonNullReasons)
                    {
                        await output.WriteLineAsync(cancellationToken);

                        foreach (var reason in nonNullReasons)
                        {
                            await output.WriteLineAsync(() => 
                                builder.Clear()
                                    .Append(outputFormat.Intendation)
                                    .Append(outputFormat.Dim)
                                    .Append(severity switch
                                    {
                                        ReportData.EntryData.EffectiveSeverity.Error        => outputFormat.Red,
