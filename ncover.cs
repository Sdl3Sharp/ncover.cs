    public string HLine
    {
        get
        {     
            var builder = mLineBuilder;

            int width;
            try { width = WindowWidth; } catch { width = mLineLengthFallback; }

            if (width < builder.Length)
            {
                builder.Remove(width, builder.Length - width);
            }
            else if (width > builder.Length)
            {
                do
                {
                    builder.Append(BoxH);
                    width -= BoxH.Length;
                }
                while (width > BoxH.Length);
                builder.Append(BoxH.AsSpan(..width));
            }

            return builder.ToString();
        }
    }
